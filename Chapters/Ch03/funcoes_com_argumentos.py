# a variavel bruce é o parametro da funcao.
# ela vai receber um valor que será atribuido a bruce
# e esse valor será passado para o corpo da funcao.
# que será impresso duas vezes.
def print_twice(bruce):
    print(bruce)
    print(bruce)

print_twice('joao')

# funcionamento:
# 'joao' será atribuido a bruce, entao teremos bruce = joao
# 'joao' será impresso duas vezes, pois é o valor que está guardado em 'bruce'
